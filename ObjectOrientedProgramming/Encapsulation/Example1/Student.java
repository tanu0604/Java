package ObjectOrientedProgramming.Encapsulation.Example1;

public class Student {
    // Class properties/instance variables
    private String name;
    private int rollno;
    private int marks;

    // Getter for name
    public String getName() {
        return name;
    }

    // Setter for name
    public void setName(String name) {
        this.name = name;
    }

    // Getter for rollno
    public int getRollno() {
        return rollno;
    }

    // Setter for rollno
    public void setRollno(int rollno) {
        this.rollno = rollno;
    }

    // Getter for marks
    public int getMarks() {
        return marks;
    }

    // Setter for marks
    public void setMarks(int marks) {
        this.marks = marks;
    }
}
